public class Continhas{
    public static void main(String args[]){
        int a, b, r;

        a = 14;
        b = 3;

        r = a + b;
        System.out.println("Valor da soma = " + r);
        r = a - b;
        System.out.println("Valor da subt = " + r);
        r = a * b;
        System.out.println("Valor da mult = " + r);
        r = a / b;
        System.out.println("Valor da div = " + r);
        r = a % b;
        System.out.println("Valor da resto = " + r);

        double x;

        x = 1/2;

        System.out.println("Valor do x = " + x);

        Float y;

        y = 1.0f/2;

        System.out.println("Valor do y= " + y);

    }
}